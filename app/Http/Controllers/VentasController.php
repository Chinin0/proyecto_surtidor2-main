    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $query=trim($request->get('searchText'));
        $ventas=DB::table('ventas as v')
            ->join('cliente as c', 'v.idcliente','=','c.ci')
            ->join('detalle_venta as dv','v.idventa','=','dv.idventa')
            ->select('v.idventa','v.fecha_hora','p.nombre','v.tipo_comprobante',
            'v.serie_comprobante','v.num_comprobante','v.impuesto','v.estado',
            'v.total_venta')
            ->where('v.num_comprobante','LIKE','%'.$query.'%')
            ->orderBy('V.idventa','desc')
            ->orderBy('V.idventa','v.fecha_hora','p.nombre','v.tipo_comprobante',
            'v.serie_comprobante','v.num_comprobante','v.impuesto','v.estado')
            ->paginate(7);
            return view('ventas.venta.index',["ventas"=>$ventas,"searchText"=>$query]);

    }

